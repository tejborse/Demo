public class OperatorExample{
	 
	   public static void main(String args[]){
		   	// declare variables
		    int a = 12, b = 5;

		    // addition operator
		    System.out.println(a + b);

		    // modulo operator
		    System.out.println("a % b = " + (a % b));
		    
		    
		    int c = 4;
		    int var;

		    // assign value using =
		    var = c;
		    System.out.println("var using =: " + var);

		    // assign value using +=
		    var += c;
		    System.out.println("var using +=: " + var);
		    
		    
		    int d = 7, e = 11;

		    // == operator
		    System.out.println(d == e);  // false

		    // != operator
		    System.out.println(d != e);  // true
	   }
	}


